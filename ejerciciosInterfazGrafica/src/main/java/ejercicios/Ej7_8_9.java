    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String nombre = txtNombre.getText();
        int ci = Integer.parseInt(txtCi.getText());
        String nacionalidad;
        ButtonModel seleccionado = grupo.getSelection();
 
        if(seleccionado == rbtnUru.getModel()){
            nacionalidad = "Uruguayo";
        }
        else{
            nacionalidad = "Extrangero";
        }
        
        if (nombre.trim().isEmpty()){
            JOptionPane.showMessageDialog(this, "Por favor, ingrese un nombre");
        }
        if (txtCi.getText().isEmpty()){
            JOptionPane.showMessageDialog(this, "Por favor, ingrese una cedula");
        }
        if (!nombre.trim().isEmpty() && !txtCi.getText().isEmpty()){
                Persona p = new Persona(nombre, ci, nacionalidad); 
                modelo.addElement(p);
        }
   
        
        verificar();
    }//GEN-LAST:event_jButton1ActionPerformed

